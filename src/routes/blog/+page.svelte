  {#each results as post (post.fake ? `fake-${post.title}` : post.slug)}
    <li>
      {#if !post.fake}
        <a href={`/blog/${post.slug}`} class="post-card">
          {#if post.thumbnail}
            <div class="post-thumb">
              <img src={post.thumbnail} alt={post.thumbnail_alt ?? ""} />
            </div>
          {/if}
