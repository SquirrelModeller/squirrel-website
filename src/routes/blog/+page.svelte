      {:else}
        <div class="post-card fake-card">
          <div class="post-body">
            <div class="post-meta">
              <span class="post-date mono muted">{post.date}</span>
              <span class="post-tag fake-tag">{post.tag}</span>
            </div>
            <p class="post-title">{post.title}</p>
          </div>
        </div>
      {/if}
    </li>
